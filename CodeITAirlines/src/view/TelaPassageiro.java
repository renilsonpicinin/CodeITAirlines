        String cpf = forCpf.getText();
        passageiroDao = new PassageiroDao();
        if(StringUtil.somenteLetras(nome) && cpf.length() == 14){
            passageiroDao.inserirPassageiro(new Passageiro(cpf, 0, nome));
        } else {
            JOptionPane.showMessageDialog(this, "CPF deve conter 11 d√≠gitos\nNome deve conter somente letras");
        }
