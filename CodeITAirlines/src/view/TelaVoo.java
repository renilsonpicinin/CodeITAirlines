    private void inicializaComboBox() {
        tripulantes = new TripulanteDao().listarTripulantes();
        for (Tripulante t : tripulantes) {
            if (t.getCargo().getDescricao().equals("Piloto")) {
                cbxPiloto.addItem(t.toString());
            } else if (t.getCargo().getDescricao().equals("Oficial")) {
                cbxOficial1.addItem(t.toString());
                cbxOficial2.addItem(t.toString());
            } else if (t.getCargo().getDescricao().equals("Chefe de Serviço")) {
                cbxChefe.addItem(t.toString());
            } else if (t.getCargo().getDescricao().equals("Comissário(a)")) {
                cbxComissaria1.addItem(t.toString());
                cbxComissaria2.addItem(t.toString());
            }
        }

        avioes = new AviaoDao().listarAviao();
        for (Aviao a : avioes) {
            cbxAviao.addItem(a.toString());
        }

        aeroportos = new AeroportoDao().listarAeroporto();
        for (Aeroporto ae : aeroportos) {
            cbxOrigem.addItem(ae.toString());
            cbxDestino.addItem(ae.toString());
        }
    }

