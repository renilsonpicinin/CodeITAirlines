    private void bttInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttInserirActionPerformed
        String pas = cbxPassageiro.getSelectedItem().toString();
        if (!pas.isEmpty()) {
            passageiros.add(new PassageiroDao().buscarPassageiro(Integer.parseInt(pas.substring(0, pas.indexOf(" ")))));
        }
        this.carregarLista();
    }//GEN-LAST:event_bttInserirActionPerformed

    private void bttSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttSalvarActionPerformed
        voo = new Voo();
        String data = ftxData.getText();
        String hora = ftxHora.getText();
        String origem = cbxOrigem.getSelectedItem().toString();
        String destino = cbxDestino.getSelectedItem().toString();
        String aviao = cbxAviao.getSelectedItem().toString();

        if (!origem.isEmpty()
                && !destino.isEmpty()
                && !aviao.isEmpty()
                && data.length() == 10 && hora.length() == 5) {

            voo.setData(data);
            voo.setHora(hora);
            voo.setOrigem(aeroportos.get(cbxOrigem.getSelectedIndex()));
            voo.setDestino(aeroportos.get(cbxDestino.getSelectedIndex()));
            voo.setAviao(avioes.get(cbxAviao.getSelectedIndex()));

            new VooDao().inserirVoo(voo);
            Integer idVoo = new VooDao().buscarVoo(voo);
            
            new TripulacaoDao().inserirTripulacao(tripulantes, idVoo);
            new PassageiroVooDao().inserirPassageiro(passageiros, idVoo);
        }
    }//GEN-LAST:event_bttSalvarActionPerformed

